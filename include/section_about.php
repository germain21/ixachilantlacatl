<p id="principal">¿QUIENES SOMOS?</p>
<div id="about">
	<p>ixachilantlacalt es una asiciacion civil que surge en el 2013 por la necesidad de rivindicar la lengua y cultura nahuatl principalmente de la delegacion Milpa alta y alrededores.</p>
	<p>organizada por varios grupos que promueven la enseñanza de la lengua nahuatl en los 12 pueblos que conforman la delegacion, desde 2005 han venido trabajando. </p>
